public class FreeFormAsciiArt {

    public static void main(String[] args) {
        drawStopSign();
    }

    // Static method to draw a simple stop sign
    public static void drawStopSign() {
        System.out.println("  _______");
        System.out.println(" /       \\");
        System.out.println("/         \\");
        System.out.println("|   GO    |");
        System.out.println("\\         /");
        System.out.println(" \\_______/");
    }
}
